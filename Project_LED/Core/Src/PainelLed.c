#include "PainelLed.h"
#include "Bsp.h"
#include "main.h"

/***********************************************************************************************************************
 * DEFINES LOCAIS
 **********************************************************************************************************************/

#define LED_BLINK_TIME    500

/***********************************************************************************************************************
 * ESTRUTURAS DE DADOS LOCAIS
 **********************************************************************************************************************/

static uint32_t previousTime = 0;

/***********************************************************************************************************************
 * FUNCOES PUBLICAS
 **********************************************************************************************************************/

/********************************************************************************************************
 * @brief   Atualiza o estado dos LEDs.
 * @details Executa a lógica de alternância do LED a cada
 *          500 ms utilizando a base de tempo do HAL.
 *
 * @warning Deve ser chamada continuamente dentro do while(1).
 ********************************************************************************************************/
void PainelLed_Update(void)
{
    uint32_t currentTime = HAL_GetTick();

    if((currentTime - previousTime) >= LED_BLINK_TIME)
    {
        previousTime = currentTime;

        BSP_LED_Toggle(0);
    }
}

